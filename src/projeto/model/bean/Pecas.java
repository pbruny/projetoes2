package projeto.model.bean;

public class Pecas {
	
	private int codigoPeca;
	private String nome;
	
	
	public int getCodigoPeca() {
		return codigoPeca;
	}
	
	public void setCodigoPeca(int codigoPeca) {
		this.codigoPeca = codigoPeca;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
}
